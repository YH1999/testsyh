package com.cn.syh.entity;

public class train_ccr {

    private int cno;
    private String cname;
    private String ctell;
    private String cport;

    public int getCno() {
        return cno;
    }

    public void setCno(int cno) {
        this.cno = cno;
    }

    public String getCname() {
        return cname;
    }

    public void setCname(String cname) {
        this.cname = cname;
    }

    public String getCtell() {
        return ctell;
    }

    public void setCtell(String ctell) {
        this.ctell = ctell;
    }

    public String getCport() {
        return cport;
    }

    public void setCport(String cport) {
        this.cport = cport;
    }
}
